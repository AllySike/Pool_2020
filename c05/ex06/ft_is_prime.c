int	ft_is_prime(int nb)
{
	int	nbr;

	nbr = 1;
	if (nb < 2)
		return (0);
	while ((nbr <= (nb + 1) / 2))
	{
		if (nb % nbr == 0 && nb != nbr && nbr != 1)
			return (0);
		nbr++;
	}
	return (1);
}
