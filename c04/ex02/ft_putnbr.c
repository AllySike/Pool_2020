#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	char	str[10];
	int		i;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		i = 0;
		if (nb < 0)
		{
			nb = nb * -1;
			ft_putchar('-');
		}
		while (nb || (nb == 0 && i == 0))
		{
			str[i] = (nb % 10) + '0';
			nb = nb / 10;
			i++;
		}
		while (i >= 0)
		{
			ft_putchar(str[i - 1]);
			i--;
		}
	}
}
