char	*ft_strcpy(char *dest, char *src)
{
	int	count;

	count = 0;
	while (src[count] != '\0')
	{
		dest[count] = src[count];
		count++;
	}
	while (dest[count] != '\0')
	{
		dest[count] = '\0';
		count++;
	}
	return (dest);
}
