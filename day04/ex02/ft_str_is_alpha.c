int	ft_str_is_alpha(char *str)
{
	int alp;
	int i;

	alp = 0;
	i = 1;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z'
				|| str[i] >= 'A' && str[i] <= 'Z')
			alp = 1;
		else
			return (0);
		i++;
	}
	return (alp);
}
