#include <stdio.h>

int	ft_strlen(char *str);

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	if (ft_strlen(s1) == ft_strlen(s2))
	{
		while (s1[i] != '\0' && s2[i] != '\0')
		{
			if (s1[i] < s2[i] || s1[i] > s2[i])
			{
				return (-1);
			}
			i++;
		}
		return (0);
	}
	return (-1);
}
